r = input('Digite algo')
print('O tipo primitivo é: ',type(r))
print('Só tem espaço ? ',r.isspace())
print('Só tem número ? ',r.isnumeric())
print('É alfabeto ? ',r.isalpha())
print('É alfanumérico',r.isalnum())
print('Está em maiúscula ',r.isupper())
print('Está em minúscula',r.islower())
print('Está capitalizada ',r.istitle())


